    void display()
    {
       int id=LoginPage.id;
       String query="select * from studfees sf,paid_fees_info pi where sf.studid=? and pi.studid=?";
       try{
          pst=con.prepareStatement(query);
          pst.setInt(1, id);
          pst.setInt(2, id);
          rs=pst.executeQuery();
          rs.next();
          txtid.setText(id+"");
          txtname.setText(rs.getString("sname"));
          txtbranch.setText(rs.getString("branch"));
          txtsession.setText(rs.getString("session"));
          txtnumber.setText(rs.getString("mobile"));
          txtgender.setText(rs.getString("gender"));
          txtfees.setText(rs.getInt("tamt")+"");
          
       }
       catch(SQLException sql)
       {
         JOptionPane.showMessageDialog(null,sql);
       }
    }
    
   
